bool isOverLimit(const int &mid, const int &x, const int &limit)
{
    ll sumTime = ((mid * (mid + 1)) / 2) * 5;

    if (sumTime + x > limit)
        return true;
    else
        return false;
}

int main()
{
    faster();
    const int LIMIT = 60 * 4;

    int nPro, kMin;
    cin >> nPro >> kMin;

    int res = 0;

    int l = 1, r = nPro;

    while (l <= r)
    {
        int mid = (l + r) / 2;
        if (isOverLimit(mid, kMin, LIMIT))
            r = mid - 1;
        else
        {
            res = mid;
            l = mid + 1;
        }
    }

    cout << res;

    return 0;
}